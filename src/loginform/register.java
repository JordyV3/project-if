    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String direccion = txtDireccion.getText();
        String username = txtUser.getText();
        String password = String.valueOf(txtContrasena.getPassword());
        if(nombre.isEmpty() || apellido.isEmpty() || direccion.isEmpty() || username.isEmpty() || password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Datos no registrados", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            userRegister(nombre, apellido, direccion, username, password);
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

    private void userRegister(String nombre, String apellido, String direccion, String username, String password) {
        Connection dbconn = DBConnection.connectDB();
        if (dbconn != null){
            try {
                PreparedStatement st = (PreparedStatement)
                        dbconn.prepareStatement("INSERT INTO user (nombre, apellido, direccion, username, password) VALUES (?, ?, ?, ?, ?)");
                st.setString(1, nombre);
                st.setString(2, apellido);
                st.setString(3,  direccion);
                st.setString(4, username);
                st.setString(5, password);
                
                int res = st.executeUpdate();
                
                JOptionPane.showMessageDialog(this, "User registrado correctamente", "OK", JOptionPane.INFORMATION_MESSAGE);
                
                txtNombre.setText("");
                txtApellido.setText("");
                txtDireccion.setText("");
                txtUser.setText("");
                txtContrasena.setText("");
                

            } catch (SQLException ex) {
                Logger.getLogger(login.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            System.out.println("La conexi√≥n no est√° disponible.");
        }
    }

